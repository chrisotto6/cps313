# Read the miles driven and the amount of gas used from the user
miles = float(input('Enter the number of miles driven: '))
gas = float(input('Enter the amount of gas used: '))

# Calculate the miles per gallon based on user input
mpg = miles / gas

# Print the result to the user
print('Your MPG for this trip was: ', mpg)